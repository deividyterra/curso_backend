// variáveis em js

/*
VAR = Maneira antiga
LET = Adiciona valor que pode ser alterado
CONST = Adiciona valor e posteriormente nao podemos usar
*/

var nome = "Deividy";
console.log(`Meu nome é: ${nome}`);

let nomeLet = "Deividy T";
nomeLet = "Terra";
console.log(`Meu nome é: ${nomeLet}`);

const nomeConst = "Deividy T";
console.log(`Meu nome é: ${nomeConst}`);